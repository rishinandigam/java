public class ArrayDemo {

    public static void main(String[] args) {
        int[] x = new int[5];
        x[0] = 11;
        x[4] = 22;
        System.out.println("Element at index 0: " + x[0]);
        System.out.println("Element at index 1: " + x[1]);
        System.out.println("Element at index 4: " + x[4]);
    }

}